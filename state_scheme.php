<h2>Punjab <span class="theme_color">Skill Development Mission (PSDM) </span></h2>



<p class="ptext">

Punjab Skill Development Mission provides free employment linked skill development trainings to the Youth in the age group of 18-35 years across the state under the various state and centrally sponsored schemes. These nationally certified trainings are provided through empanelled training partners at centres in urban and rural areas across the state in over 20 different sectors like Plumbing, Healthcare, Media & Entertainment, Green Jobs, Domestic Workers, Retail, Beauty, Construction, Electronics and Hardware, Food Processing, Health Care, IT ITES, Leather, Logistics etc. Mission has been constantly bridging the gap between skilled people required in Industry and unemployed youth by ensuring that the right candidate chooses the right course according to his/her academic background, aptitude and skill-set. PSDM in its ambit covers various kinds of schemes thereby ensuring universal coverage from various sections of the society including Urban/ Rural, Border Area, traditional courses like Phulkari Jutti making/ modern courses thereby providing opportunity to all.</p>



<p class="ptext">Official Website : <a href="http://psdm.gov.in/" target="_new">Click Here</a></p>





