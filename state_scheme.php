	<div id="scheme09">
		<h2>Uttar Pradesh <span class="theme_color"> Skill Development Mission</span></h2>	
		<p class="ptext">A National Skill Development Policy was launched in 2009 with the aim of skilling 500 million persons by 2022. Under the National Plan, the State of Uttar Pradesh aims to skill over 4 million youth by the end of the 12th Five Year Plan. In order to achieve this target and provide employable skills to the youth of the State, the Uttar Pradesh Skill Development Mission (UPSDM) has been instituted. Based on the learnings and experiences of other states, the Government of Uttar Pradesh has developed a unique structure for UPSDM. UPSDM acts as an integrated mission which combines the efforts of various State Departments in achieving the skill development targets for the State. UPSDM is the implementing agency for skill development targeted at enhancing employability in Uttar Pradesh, subsuming the targets of various State Departments such as Rural Development, Urban Development, Labour, Minority Welfare, Social Welfare, etc. Established in 2013; mandated to coordinate all skill development initiatives, leveraging on State Skill Development Policy, UPSDM empanelled Private Training Partners in addition to Govt. Training Partners for conducting skill development trainings.</p>
	</div>



