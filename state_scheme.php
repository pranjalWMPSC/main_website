<h2>Haryana <span class="theme_color">Skill Development Mission (HSDM) </span></h2>



<p class="ptext">

Haryana Skill Development Mission (HSDM) has been established in May 2015 by State Government with an aim to empower youth to take part in the economic and all around growth of HARYANA AND INDIA. The Mission is a single point of contact within the Government to formulate and steer skill development schemes across the Department. HSDM acts as an integrated mission which combines the efforts of various State Departments in achieving the skill development target of the State.

</p>

<p class="ptext">HSDM already have 80+ courses, which covers 15+ different sectors available for the youth of Haryana in our schemes like SURYA, SAKSHAM, PMKVY etc.<br>

The Mission also has Entrepreneurship scheme which is currently being implemented through HVSU. Equipped with a team of professionals and cutting edge Training partners, each program is implemented with the vision of developing futuristic knowledge, skills and hence gaining the employment or becoming a successful entrepreneur.</p>





<p class="ptext">Official Website : <a href="https://hsdm.org.in/" target="_new">Click Here</a></p>





