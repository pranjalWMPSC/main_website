		<h2>Jharkhand <span class="theme_color"> Skill Development Mission Society (JSDMS)</span></h2>	
		<p class="ptext">Jharkhand Skill Development Mission Society (JSDMS) acts as the nodal organization for all Skill Development related activities in the state. Working on a delivery-led model, JSDMS is pursuing a target of fresh skilling, re-skilling and upskilling of 20 lakh youths of the state by 2022. With such a massive goal to be achieved, the Skill Mission has adeptly aligned all efforts and initiatives to result-oriented practices and interventions, for youth aged between 18 and 35 years.<br/> The Mission, which has three programs under its banner, namely Saksham Jharkhand Kaushal Vikas Yojana (SJKVY), Deen Dayal Upadhyaya Kaushal Kendra (DDU-KK) popularly known as Mega Skill Centers, and Employability Excellence with College Education & Learning (EXCEL) & Recognition of Prior Learning (RPL) works on the Public-Private Partnership Model (PPP) and collaborates with private domain training providers referred to as Training Service Providers (TSPs) under the Jharkhand Skill Development Mission Society (JSDMS) ecosystem.</p>
