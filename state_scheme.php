<h2>MPSSDEGB</h2>



<p class="ptext">

MPSSDEGB has been acting as the nodal agency for all the Skill Development initiatives in the State of Madhya Pradesh. In order to ensure that the State is able to translate the large availability of working age population into a demographic advantage, the State has launched the Mukhya Mantri Kaushal Samvardhan Yojna (MMKSY). MMKSY aims at raising the skilling quotient of the youth in the State by providing access to short term demand driven training courses in employable trades.A similar scheme “Mukhya Mantri Kaushalya Yojna (MMKY)” has also been launched exclusively for women of the State for skill development and enhancing their employment quotient with an aim of increasing financial independence of women. MMKSY & MMKY aim at providing the ‘Skill Set’ which is sufficient to allow an individual to be gainfully employed or self-employed. These schemes have been launched with an overall target of 4,50,000 candidates.</p>



