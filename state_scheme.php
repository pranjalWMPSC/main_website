		<ul class="ptext">
			<li>1. An apex body of all skill initiatives in the state to achieve skilling target of 1.50 lakhs youths in a year.</li>
			<li>2. To provide quality skill training for gainful employment of educated unemployed youth.</li>
			<li>3. To encourage development of entrepreneurs in different sectors.</li>
			<li>4. To implement central sponsored skilling schemes in the state.</li>
