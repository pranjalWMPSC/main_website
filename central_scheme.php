<h2>DDU-GKY<span class="theme_color"> (Deen Dayal Upadhyaya Grameen Kaushalya Yojana)</span></h2>	

<p class="ptext">The Ministry of Rural Development (MoRD) announced the Deen Dayal Upadhyaya Grameen Kaushalya Yojana (DDU-GKY) Antyodaya Diwas, on 25th September 2014. DDU-GKY is a part of the National Rural Livelihood Mission (NRLM), tasked with the dual objectives of adding diversity to the incomes of rural poor families and cater to the career aspirations of rural youth.</p>

<p class="ptext">DDU-GKY is uniquely focused on rural youth between the ages of 15 and 35 years from poor families. As a part of the Skill India campaign, it plays an instrumental role in supporting the social and economic programs of the government like the Make In India, Digital India, Smart Cities and Start-Up India, Stand-Up India campaigns. Over 180 million or 69% of the country’s youth population between the ages of 18 and 34 years, live in its rural areas. Of these, the bottom of the pyramid youth from poor families with no or marginal employment number about 55 million.</p>

<p class="ptext">The National Policy for Skill Development & Entrepreneurship 2015 has identified a skills gap of 109.73 million in 24 key sectors by the year 2022. This number cannot be achieved without addressing the BoP 55 million from rural India. Also, a FICCI and Ernst – Young study published in 2013 identified a shortage of over 47 million skilled workers across the globe by 2020. This presents an unprecedented opportunity for India to train its BoP youth population and place them in jobs across the world and realize its demographic dividend.</p>

<p class="ptext">DDU-GKY takes pride in its partners and their ability to add value. Innovation from partners is encouraged to build scale and capacity… its unique implementation structure involves partners, who are by nature, committed to changing lives and are experts in their areas, they form a part of the Skilling Ecosystem integrated by DDU-GKY. Partners are supported through investment, capacity building, strategies for retention, linkages to international placement and technology support for training purposes.</p>

<p class="ptext">DDU-GKY is present in 21 States and UTs, across 568 districts, impacting youth from over 6,215 blocks. It currently has over 690 projects being implemented by over 300 partners, in more than 330 trades from 82 industry sectors. Over 2.7 Lakh candidates have been trained and over 1.34 Lakh candidates have been placed in jobs so in the course of the last financial year. From 2012, DDU-GKY has so far committed an investment of more than INR 5,600 Crores, impacting rural youth pan-India.</p>


